import { mkdir, rm } from "node:fs/promises";
import { build } from "esbuild";

const slugs = [
  "judge-claim",
  "wizard-turn",
  "create-room",
  "submit-argument",
  "advance-wizard",
  "get-room",
  "list-rooms",
  "leaderboard",
  "record-match",
  "health",
];

const outdir = "backend/functions/dist";

await rm(outdir, { recursive: true, force: true });
await mkdir(outdir, { recursive: true });

await Promise.all(
  slugs.map((slug) =>
    build({
      entryPoints: [`backend/functions/src/entrypoints/${slug}.ts`],
      outfile: `${outdir}/${slug}.ts`,
      bundle: true,
      format: "esm",
      platform: "browser",
      target: "es2022",
      legalComments: "none",
      treeShaking: true,
      banner: {
        js: `// Generated by npm run build:functions. Deploy with: npx @insforge/cli functions deploy ${slug} --file backend/functions/dist/${slug}.ts`,
      },
    }),
  ),
);

console.log(`Built ${slugs.length} function bundles in ${outdir}`);
